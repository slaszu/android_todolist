@Composable
fun TodoAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Ustawiamy domyślnie na false, aby Twoja zielona stylistyka była widoczna
    // zamiast kolorów systemowych Androida (Material You)
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> darkScheme
        else -> lightScheme
    }

    MaterialTheme(
        colorScheme = colorScheme,
        // Upewnij się, że TodoAppTypography jest zdefiniowane w pliku Type.kt
        typography = TodoAppTypography,
        content = content
    )
}